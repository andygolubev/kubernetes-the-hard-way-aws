# From A Cloud Guru course "Kubernetes the Hard Way" https://www.pluralsight.com/cloud-guru/courses/kubernetes-the-hard-way

# The next step in building a Kubernetes cluster the hard way is to generate kubeconfigs which will 
# be used by the various services that will make up the cluster. In this lesson, we will generate these kubeconfigs. 
# After completing this lesson, you should have a set of kubeconfigs which you will need later in order 
# to configure the Kubernetes cluster.

# Here are the commands used in the demo. Be sure to replace the placeholders with actual values from your cloud servers.

# Create an environment variable to store the address of the Kubernetes API, and set it to the private IP of your load balancer cloud server:

KUBERNETES_ADDRESS=172.20.0.4

# Generate a kubelet kubeconfig for each worker node:

for instance in working-node-0 working-node-1 working-node-2; do
  kubectl config set-cluster kubernetes-the-hard-way \
    --certificate-authority=ca.pem \
    --embed-certs=true \
    --server=https://${KUBERNETES_ADDRESS}:6443 \
    --kubeconfig=${instance}.kubeconfig
  kubectl config set-credentials system:node:${instance} \
    --client-certificate=${instance}.pem \
    --client-key=${instance}-key.pem \
    --embed-certs=true \
    --kubeconfig=${instance}.kubeconfig
  kubectl config set-context default \
    --cluster=kubernetes-the-hard-way \
    --user=system:node:${instance} \
    --kubeconfig=${instance}.kubeconfig

  kubectl config use-context default --kubeconfig=${instance}.kubeconfig
done

