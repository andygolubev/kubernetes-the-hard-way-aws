# From A Cloud Guru course "Kubernetes the Hard Way" https://www.pluralsight.com/cloud-guru/courses/kubernetes-the-hard-way

# Before you can stand up controllers for a Kubernetes cluster, you must first build an etcd 
# cluster across your Kubernetes control nodes. This lesson provides a demonstration of how to s
# et up an etcd cluster in preparation for bootstrapping Kubernetes. After completing this lesson, 
# you should have a working etcd cluster that consists of your Kubernetes control nodes.

# Here are the commands used in the demo (note that these have to be run on both controller 
# servers, with a few differences between them):

cd /tmp/kthw-certs

{

INITIAL_CLUSTER=control-plane-0=https://172.20.0.5:2380,control-plane-1=https://172.20.16.5:2380,control-plane-2=https://172.20.32.5:2380


ETCD_NAME=control-plane-0
INTERNAL_IP=172.20.0.5

cat > etcd.service-0 << EOF
[Unit]
Description=etcd
Documentation=https://github.com/coreos

[Service]
ExecStart=/usr/local/bin/etcd \\
  --name ${ETCD_NAME} \\
  --cert-file=/etc/etcd/kubernetes.pem \\
  --key-file=/etc/etcd/kubernetes-key.pem \\
  --peer-cert-file=/etc/etcd/kubernetes.pem \\
  --peer-key-file=/etc/etcd/kubernetes-key.pem \\
  --trusted-ca-file=/etc/etcd/ca.pem \\
  --peer-trusted-ca-file=/etc/etcd/ca.pem \\
  --peer-client-cert-auth \\
  --client-cert-auth \\
  --initial-advertise-peer-urls https://${INTERNAL_IP}:2380 \\
  --listen-peer-urls https://${INTERNAL_IP}:2380 \\
  --listen-client-urls https://${INTERNAL_IP}:2379,https://127.0.0.1:2379 \\
  --advertise-client-urls https://${INTERNAL_IP}:2379 \\
  --initial-cluster-token etcd-cluster-0 \\
  --initial-cluster ${INITIAL_CLUSTER} \\
  --initial-cluster-state new \\
  --data-dir=/var/lib/etcd
Restart=on-failure
RestartSec=5

[Install]
WantedBy=multi-user.target
EOF


